@Controller
public class IndexController {
	@Resource
	CarouselService carouselService;
	
	/**
	 * 查看
	 * @author lucky(李文鑫) Email:578581198@qq.com
	 * 2017年7月17日-下午12:03:42
	 * @param carousel
	 * @param request
	 * @param session
	 * @return
	 */
	@RequestMapping(value="getIndexCarouseData",method=RequestMethod.POST)
	@ResponseBody
	public List<Carousel> getIndexCarouseData(){
		return FirstData.carousels;
	}
	
	/**
	 * 公司文化
	 * @author lucky(李文鑫) Email:578581198@qq.com
	 * 2017年7月18日-上午11:03:53
	 * @param model
	 * @return
	 */
	@RequestMapping(value="toCorporateCulturePage",method=RequestMethod.GET)
	public String toCorporateCulturePage(Model model){
		try {
			model.addAttribute("corporates", FirstData.corporate_culture);
			return "pc/corporate_culture";
		} catch (Exception e) {
			System.out.println("查看公司文化:"+e.getMessage());
			return null;
		}
	}
}
