@Component
public class Interceptor extends HandlerInterceptorAdapter {
	/**
	 * 执行之前
	 */
	@Override
	public boolean preHandle(HttpServletRequest request,
			HttpServletResponse response, Object handler) throws Exception {
		// handler 拦截的内容
		// HandlerMethod 拦截了一个方法 获取到方法的所有东西
		String type = request.getMethod();
		HandlerMethod hm = null;
		if (handler instanceof HandlerMethod) {
			hm = ((HandlerMethod) handler);
		} else {
			return true;
		}
		// 获取权限注解 获取到注解的内容
		String cs = hm.getBean().getClass().getName();
		if (cs.indexOf("com.ymkj.admin.controller") > -1 && type.equals("POST")) {
			String method = hm.getMethod().getName();
			if ("verificationCode".equals(method)
					|| "getVerificationStream".equals(method)
					|| "loginAdmin".equals(method) || "loginto".equals(method)
					|| "cancellationAdmin".equals(method)) {
				return true;
			}
			if (request.getSession().getAttribute("administrator") != null) {
				return true;
			}
			response.getWriter().write("LOGIN");
			return false;
		}
		return true;
	}
}
