@Controller
public class AdminClassic_caseController {
	@Resource
	Classic_caseService classic_caseService;
	
	/**
	 * 添加经典案例
	 * @author lucky(李文鑫) Email:578581198@qq.com
	 * 2017年7月14日-上午11:58:05
	 * @param session
	 * @param classic_case
	 * @return
	 */
	@RequestMapping(value="addClassic_caseData",method=RequestMethod.POST)
	@ResponseBody
	public Map<String, Object> addClassic_caseData(HttpSession session,@ModelAttribute Classic_case classic_case){
		return classic_caseService.addClassicCase(classic_case, session);
	}
	
	/**
	 * 修改经典案例
	 * @author lucky(李文鑫) Email:578581198@qq.com
	 * 2017年7月14日-上午11:58:44
	 * @param session
	 * @param classic_case
	 * @return
	 */
	@RequestMapping(value="updClassic_caseData",method=RequestMethod.POST)
	@ResponseBody
	public Map<String, Object> updClassic_caseData(HttpSession session,@ModelAttribute Classic_case classic_case){
		return classic_caseService.updClassicCaseContent(classic_case, session);
	}
}
